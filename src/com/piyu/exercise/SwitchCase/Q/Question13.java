package com.piyu.exercise.SwitchCase.Q;

public class Question13 {
    public static void main(String[] args) {
        char ch = 'a';
        switch (ch)
        {
            case 'a':
            case 'A':
                System.out.println(ch);
                break;
            case 'b':
            case 'B':
                System.out.println(ch);
                break;
            case 'c':
            case 'C':
                System.out.println(ch);
                break;
            case 'd':
            case 'D':
                System.out.println(ch);
        }
    }
}
