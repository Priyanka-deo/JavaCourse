package com.piyu.exercise;

public class Exercise11 {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(a);
    }
}
